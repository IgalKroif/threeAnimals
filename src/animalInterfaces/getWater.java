package animalInterfaces;

/**
 * The interface getWater represents an animal that lives in water.
 */
public interface getWater {

   /**
    * Checks if the animal has gills.
    *
    * @return true if the animal has gills, false otherwise.
    */
   boolean hasGills();

   /**
    * Checks if the animal lays eggs.
    *
    * @return true if the animal lays eggs, false otherwise.
    */
   boolean doesLayEggs();
}